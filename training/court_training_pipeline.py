      pairs, missing, bad = [], 0, 0

      for item in data:
          stem = item.get("id")
          kps  = item.get("kps")
          if not stem or not kps or len(kps) != 14:
              bad += 1
              continue

          img_path = find_img_for_id(stem)
          if not img_path:
              missing += 1
              continue

