def coco_to_yolo_pose(coco_json_path: Path, img_dst: Path, lbl_dst: Path, num_kp=14, pad=0.05):
    coco = json.loads(coco_json_path.read_text())
    # Build image dict
    id2img = {im["id"]: im for im in coco["images"]}
    # Group anns per image_id (expect one court per image; if more, we’ll write multiple rows)
    img2anns = {}
    for ann in coco["annotations"]:
        if ann.get("num_keypoints", num_kp) != num_kp:
            continue
        img2anns.setdefault(ann["image_id"], []).append(ann)

    # Copy images and write labels
    src_dir = coco_json_path.parent
    kept, skipped = 0, 0
    for img_id, im in id2img.items():
        fname = im["file_name"]
        src = src_dir/fname
        if not src.exists():
            skipped += 1
            continue
        # copy image
        dst = img_dst/fname
        if not dst.exists():
            shutil.copy2(src, dst)

        W, H = Image.open(dst).size
        lbl_path = lbl_dst/(Path(fname).stem + ".txt")
        rows = []

        for ann in img2anns.get(img_id, []):
            kps = ann.get("keypoints", [])
            # Expect [x1,y1,v1,...,x14,y14,v14] in COCO; if your JSON had [x,y] only, you already set v=2 in your exporter
            if len(kps) != num_kp*3:
                continue
            xs = [kps[i] for i in range(0, num_kp*3, 3)]
            ys = [kps[i] for i in range(1, num_kp*3, 3)]
            vs = [kps[i] for i in range(2, num_kp*3, 3)]

            # derive bbox from visible/occluded points; fall back to all points if needed
            use_idx = [i for i,v in enumerate(vs) if v > 0]
            if not use_idx: use_idx = list(range(num_kp))
            xmin, xmax = min(xs[i] for i in use_idx), max(xs[i] for i in use_idx)
            ymin, ymax = min(ys[i] for i in use_idx), max(ys[i] for i in use_idx)
            # pad
            pw, ph = (xmax-xmin), (ymax-ymin)
            xmin -= pad*max(pw,ph); xmax += pad*max(pw,ph)
            ymin -= pad*max(pw,ph); ymax += pad*max(pw,ph)
            xmin, ymin = max(0.0, xmin), max(0.0, ymin)
            xmax, ymax = min(W-1.0, xmax), min(H-1.0, ymax)

            # xywh (normalized)
            w = max(1.0, xmax - xmin); h = max(1.0, ymax - ymin)
            cx = (xmin + xmax) / 2.0; cy = (ymin + ymax) / 2.0
            row = [0, cx/W, cy/H, w/W, h/H]  # class=0 ("court")

            # keypoints normalized + visibility
            for i in range(num_kp):
                px = min(max(xs[i]/W, 0.0), 1.0)
                py = min(max(ys[i]/H, 0.0), 1.0)
                v  = int(vs[i]) if isinstance(vs[i], (int,float)) else 2
                # clamp v to {0,1,2}
                v = 0 if v<=0 else (1 if v==1 else 2)
                row += [px, py, v]

            rows.append(" ".join(str(round(x,6)) if isinstance(x,float) else str(x) for x in row))

        if rows:
            lbl_path.write_text("\n".join(rows))
            kept += 1
    print(f"Converted: labels for {kept} images, skipped {skipped} (missing image or no valid kp).")

coco_to_yolo_pose(COCO_TRAIN, IMG_TRAIN, LBL_TRAIN, num_kp=14, pad=0.06)
